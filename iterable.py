# Iterables = An object/collection that can return its elements one at a time, allowing it to be iterated over in a loop.

# Examples of iterables in Python include lists, tuples, strings, dictionaries, and sets.

# Example code

my_dict = {"Name" : "Sahaj Singh", "Age" : 22, "City" : "Kota"}

for key, value in my_dict.items():
    print(f"{key} : {value}")