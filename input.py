#The input function always returns a string, so we need to typecast it to the desired data type
age = int(input("Enter your age: ")) #typecasting the input to int
print(age) #prints the age as an integer
height = float(input("Enter your height in meters: ")) #typecasting the input to float
print(height) #prints the height as a float
name = input("Enter your name: ") #no typecasting needed as we want a string
print(name) #prints the name as a string