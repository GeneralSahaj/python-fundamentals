#Madlibs Game
# word game where you create a story template and ask the user for words to fill in the blanks

print("Welcome to the Madlibs Game!")



# Ask the user for input to fill in the blanks
adjective = input("Enter an adjective: ")
animal = input("Enter an animal: ")
verb_ing = input("Enter a verb ending in -ing: ")
adverb = input("Enter an adverb: ")
name_of_person = input("Enter the name of a person: ")
emotion = input("Enter an emotion: ")
adjective2 = input("Enter another adjective: ")
food = input("Enter a type of food: ")
emotion2 = input("Enter another emotion: ")

print(f"Today, I went to the zoo and saw a {adjective} {animal} jumping up and down in its tree.")
print(f"It looked like it was {verb_ing} {adverb} just for fun.")
print(f"but then I realized it looked like {name_of_person} which made me feel {emotion}.")
print(f"so i gave {name_of_person} a {adjective2} {food} as a treat and then i went home feeling {emotion2}.")